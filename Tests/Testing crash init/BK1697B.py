import MOKO


#######################################################################################################################
#######################################################################################################################
#######################################################################################################################
#######################################################################################################################
#######################################################################################################################

class BK1697B:

#######################################################################################################################
###########################################  BK1697B Initialization  ##################################################
#######################################################################################################################

    def Initialization(self) -> None:

        MOKO.Stage("*********************************************************")
        MOKO.Stage("***************** Init device BK1697B *******************")
        MOKO.Stage("*********************************************************")
        MOKO.Stage(" ")

        self.INIT_DEVICE()

#######################################################################################################################
#############################################  BK1697B Init device  ###################################################
#######################################################################################################################

    def INIT_DEVICE(self) -> str:
        return MOKO.Driver('BK1697B', 'init', '')

#######################################################################################################################
#####################################  BK1697B NOT SUCCESFUL Init device  #############################################
#######################################################################################################################

    @staticmethod
    def CHOICES() -> bool:
        return MOKO.Messenger("get", "Continue measuring?#@attention",
                              "Do you want to continue measuring", "boolean")

#######################################################################################################################
###############################################  BK1697B SET VALUE  ###################################################
#######################################################################################################################

    def SET_VALUE(self, value: (float | int | str)) -> None:
        MOKO.Driver('BK1697B', 'set', f'VDC = {value}')

#######################################################################################################################
#############################################  BK1697B SET OUTPUT ON  #################################################
#######################################################################################################################

    def SET_OUTPUT_ON(self) -> None:
        MOKO.Driver('BK1697B', 'set', 'OUTPUT = ON')
        MOKO.Stage(" ")

#######################################################################################################################
############################################  BK1697B SET OUTPUT OFF  #################################################
#######################################################################################################################

    def SET_OUTPUT_OFF(self) -> None:
        MOKO.Driver('BK1697B', 'set', 'OUTPUT = OFF')
        MOKO.Stage(" ")